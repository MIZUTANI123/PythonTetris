    def get_cell_data(self, type, rot, x, y):
        """
        指定されたblockの1マスのデータを取得。
        type:ブロックの種類
        rot:ブロックの回転回数
        x:ブロックセルのx軸
        y:ブロックセルのy軸
        """
        return self.blocks[type][rot][y][x]

